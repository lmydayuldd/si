package com.sidc.rcu.hmi.groupdevice.bean;

import java.io.Serializable;

public class RcuGroupDeviceBean implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = -8358545646099950636L;
	private int groupid;

	public int getGroupid() {
		return groupid;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("RcuGroupDeviceBean [groupid=");
		builder.append(groupid);
		builder.append("]");
		return builder.toString();
	}

	public RcuGroupDeviceBean(int groupid) {
		super();
		this.groupid = groupid;
	}
}

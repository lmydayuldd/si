package com.sidc.rcu.hmi.websocket.bean;

import java.io.Serializable;

public class RoomControlDevicesWebsocketBean implements Serializable {
	private static final long serialVersionUID = -1033168482789304059L;
	private String keycode;
	private String status;
	private String speed;
	private String temperature;

	public String getKeycode() {
		return keycode;
	}

	public String getStatus() {
		return status;
	}

	public String getSpeed() {
		return speed;
	}

	public String getTemperature() {
		return temperature;
	}

	public RoomControlDevicesWebsocketBean(String keycode, String status, String speed, String temperature) {
		super();
		this.keycode = keycode;
		this.status = status;
		this.speed = speed;
		this.temperature = temperature;
	}

	public RoomControlDevicesWebsocketBean(String keycode, String status, String speed) {
		super();
		this.keycode = keycode;
		this.status = status;
		this.speed = speed;
	}

	public RoomControlDevicesWebsocketBean(String keycode, String status) {
		super();
		this.keycode = keycode;
		this.status = status;
	}
}

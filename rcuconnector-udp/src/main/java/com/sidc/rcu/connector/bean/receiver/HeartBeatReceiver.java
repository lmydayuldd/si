package com.sidc.rcu.connector.bean.receiver;

/**
 * @author Nandin
 *
 */
public class HeartBeatReceiver implements java.io.Serializable {

	private static final long serialVersionUID = -3581879013973764272L;

	private boolean status;
	public HeartBeatReceiver(boolean status) {
		this.status = status;
	}
	public boolean isStatus() {
		return status;
	}

	public String toString() {
		
		StringBuilder builder = new StringBuilder();
		builder.append("HeartBeatReceiver [status=\n");
		builder.append(status);
		builder.append("]");
		return builder.toString();
	}
}

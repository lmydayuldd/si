package com.sidc.blackcore.api.sits.shop.bean;

import java.io.Serializable;

public class ShopLangBean implements Serializable {
	private static final long serialVersionUID = -2608396143355710498L;
	private String name;
	private String langcode;
	private String description;

	public String getName() {
		return name;
	}

	public String getLangcode() {
		return langcode;
	}

	public String getDescription() {
		return description;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("ShopLangBean [name=");
		builder.append(name);
		builder.append(", langcode=");
		builder.append(langcode);
		builder.append(", description=");
		builder.append(description);
		builder.append("]");
		return builder.toString();
	}

	public ShopLangBean(String name, String langcode, String description) {
		super();
		this.name = name;
		this.langcode = langcode;
		this.description = description;
	}

}

package com.sidc.blackcore.api.sits.movie.bean;

import java.io.Serializable;

public class MovieInfoBean implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1046946598919752797L;
	private String lang;
	private String name;
	private String director;
	private String actor;
	private String introduction;
	private String subtitle;

	public String getLang() {
		return lang;
	}

	public String getName() {
		return name;
	}

	public String getDirector() {
		return director;
	}

	public String getActor() {
		return actor;
	}

	public String getIntroduction() {
		return introduction;
	}

	public String getSubtitle() {
		return subtitle;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("MovieInfoBean [lang=");
		builder.append(lang);
		builder.append(", name=");
		builder.append(name);
		builder.append(", director=");
		builder.append(director);
		builder.append(", actor=");
		builder.append(actor);
		builder.append(", introduction=");
		builder.append(introduction);
		builder.append(", subtitle=");
		builder.append(subtitle);
		builder.append("]");
		return builder.toString();
	}

	public MovieInfoBean(String lang, String name, String director, String actor, String introduction,
			String subtitle) {
		super();
		this.lang = lang;
		this.name = name;
		this.director = director;
		this.actor = actor;
		this.introduction = introduction;
		this.subtitle = subtitle;
	}

}

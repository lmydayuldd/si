package com.sidc.blackcore.api.mobile.laundry.request;

import java.io.Serializable;

public class LaundryWashTypeRequest implements Serializable {
	private static final long serialVersionUID = 6117956886907992249L;
	private String langcode;
	private String status;

	public String getLangcode() {
		return langcode;
	}

	public String getStatus() {
		return status;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("LaundryWashTypeRequest [langcode=");
		builder.append(langcode);
		builder.append(", status=");
		builder.append(status);
		builder.append("]");
		return builder.toString();
	}

	public LaundryWashTypeRequest(String langcode, String status) {
		super();
		this.langcode = langcode;
		this.status = status;
	}

}

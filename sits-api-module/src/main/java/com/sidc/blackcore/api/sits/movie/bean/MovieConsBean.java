package com.sidc.blackcore.api.sits.movie.bean;

import java.io.Serializable;

public class MovieConsBean implements Serializable {
	private static final long serialVersionUID = -5140777560939674201L;
	private String moviename;
	private int playtime;

	public String getMoviename() {
		return moviename;
	}

	public int getPlaytime() {
		return playtime;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("MovieConsBean [moviename=");
		builder.append(moviename);
		builder.append(", playtime=");
		builder.append(playtime);
		builder.append("]");
		return builder.toString();
	}

	public MovieConsBean(String moviename, int playtime) {
		super();
		this.moviename = moviename;
		this.playtime = playtime;
	}

}

package com.sidc.blackcore.api.sits.weather.bean;

import java.io.Serializable;

/**
 * @author Joe
 *
 */
public class OpenWeatherMapWindBean implements Serializable {
	private static final long serialVersionUID = -7781073516643394941L;
	private float speed;
	private float deg;

	public float getSpeed() {
		return speed;
	}

	public float getDeg() {
		return deg;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("OpenWeatherMapWindBean [speed=");
		builder.append(speed);
		builder.append(", deg=");
		builder.append(deg);
		builder.append("]");
		return builder.toString();
	}

	public OpenWeatherMapWindBean(float speed, float deg) {
		super();
		this.speed = speed;
		this.deg = deg;
	}

}

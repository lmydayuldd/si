package com.sidc.blackcore.api.sits.userauthorization.bean;

import java.io.Serializable;

/**
 * @author Joe
 *
 */
public class AuthorizationFunctionInfoBean implements Serializable {
	private static final long serialVersionUID = -5414670273563320335L;
	private String funcitonid;
	private String functiondesc;
	private String authorizationtype;

	public String getFuncitonid() {
		return funcitonid;
	}

	public String getFunctiondesc() {
		return functiondesc;
	}

	public String getAuthorizationtype() {
		return authorizationtype;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("AuthorizationFunctionInfoBean [funcitonid=");
		builder.append(funcitonid);
		builder.append(", functiondesc=");
		builder.append(functiondesc);
		builder.append(", authorizationtype=");
		builder.append(authorizationtype);
		builder.append("]");
		return builder.toString();
	}

	public AuthorizationFunctionInfoBean(String funcitonid, String functiondesc, String authorizationtype) {
		super();
		this.funcitonid = funcitonid;
		this.functiondesc = functiondesc;
		this.authorizationtype = authorizationtype;
	}

}

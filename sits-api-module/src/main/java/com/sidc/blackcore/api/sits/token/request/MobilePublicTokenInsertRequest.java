package com.sidc.blackcore.api.sits.token.request;

import java.io.Serializable;

public class MobilePublicTokenInsertRequest implements Serializable {

	private static final long serialVersionUID = -353729078704004913L;

	private String roomno;
	private int type;

	public String getRoomno() {
		return roomno;
	}

	public int getType() {
		return type;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("MobilePublicTokenInsertRequest [roomno=");
		builder.append(roomno);
		builder.append(", type=");
		builder.append(type);
		builder.append("]");
		return builder.toString();
	}

	public MobilePublicTokenInsertRequest(String roomno, int type) {
		super();
		this.roomno = roomno;
		this.type = type;
	}

}

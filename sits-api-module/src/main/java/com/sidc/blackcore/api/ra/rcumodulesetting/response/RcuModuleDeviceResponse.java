package com.sidc.blackcore.api.ra.rcumodulesetting.response;

import java.io.Serializable;

public class RcuModuleDeviceResponse implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = -3902600165039991386L;
	private int groupId;
	private int deviceId;
	private String device;

	public int getGroupId() {
		return groupId;
	}

	public int getDeviceId() {
		return deviceId;
	}

	public String getDevice() {
		return device;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("RcuModuleDeviceResponse [groupId=");
		builder.append(groupId);
		builder.append(", deviceId=");
		builder.append(deviceId);
		builder.append(", device=");
		builder.append(device);
		builder.append("]");
		return builder.toString();
	}

	public RcuModuleDeviceResponse(int groupId, int deviceId, String device) {
		super();
		this.groupId = groupId;
		this.deviceId = deviceId;
		this.device = device;
	}

}

package com.sidc.blackcore.api.mobile.message.request;

import java.io.Serializable;

public class NewslettersSelectRequest implements Serializable {
	private static final long serialVersionUID = -1313153612130854733L;
	private String deviceid;
	private String newslettersid;

	public String getDeviceid() {
		return deviceid;
	}

	public String getNewslettersid() {
		return newslettersid;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("NewslettersSelectRequest [deviceid=");
		builder.append(deviceid);
		builder.append(", newslettersid=");
		builder.append(newslettersid);
		builder.append("]");
		return builder.toString();
	}

	public NewslettersSelectRequest(String deviceid, String newslettersid) {
		super();
		this.deviceid = deviceid;
		this.newslettersid = newslettersid;
	}

}

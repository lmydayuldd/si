package com.sidc.blackcore.api.mobile.laundry.bean;

import java.io.Serializable;

public class LaundryTypeBean implements Serializable {
	private static final long serialVersionUID = 2387057272441616902L;
	private int id;
	private int status;

	public int getId() {
		return id;
	}

	public int getStatus() {
		return status;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("LaundryTypeBean [id=");
		builder.append(id);
		builder.append(", status=");
		builder.append(status);
		builder.append("]");
		return builder.toString();
	}

	public LaundryTypeBean(int id, int status) {
		super();
		this.id = id;
		this.status = status;
	}

}

package com.sidc.dao.ra.response;

import java.io.Serializable;

/**
 * @author Nandin
 *
 */
public class RcuModule implements Serializable {

	private static final long serialVersionUID = -6025929290062581554L;
	private int id;
	private String device;

	public RcuModule(int id, String device) {
		super();
		this.id = id;
		this.device = device;
	}

	public int getId() {
		return id;
	}

	public String getDevice() {
		return device;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("RcuModule [\n\tid=");
		builder.append(id);
		builder.append(", device=");
		builder.append(device);
		builder.append("]");
		return builder.toString();
	}

}

package com.sidc.rcu.connector.bean.command;

public class BulbCommander implements java.io.Serializable {

	private static final long serialVersionUID = -5624182338679989812L;
	private int value;

	public BulbCommander(int value) {
		super();
		this.value = value;
	}


	public int getValue() {
		return value;
	}


	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("BulbCommander [\n\tvalue=");
		builder.append(value);
		builder.append("]");
		return builder.toString();
	}

}

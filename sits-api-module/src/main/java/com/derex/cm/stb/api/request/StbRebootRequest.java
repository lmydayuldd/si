package com.derex.cm.stb.api.request;

import java.io.Serializable;
import java.util.List;

/**
 * @author Nandin
 *
 */
public class StbRebootRequest implements Serializable {

    /**
     * 
     */
    private static final long serialVersionUID = 9210602959985743011L;
    private List<String> stbip;

    public StbRebootRequest(List<String> stbip) {
	super();
	this.stbip = stbip;
    }

    public List<String> getStbip() {
	return stbip;
    }

    @Override
    public String toString() {
	StringBuilder builder = new StringBuilder();
	builder.append("StbRebootRequest [stbip=\n");
	builder.append(stbip);
	builder.append("]");
	return builder.toString();
    }

}

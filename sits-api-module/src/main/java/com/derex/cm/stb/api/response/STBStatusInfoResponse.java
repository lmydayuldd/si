package com.derex.cm.stb.api.response;

import java.util.List;

public class STBStatusInfoResponse implements java.io.Serializable {

	private static final long serialVersionUID = 5671861626102574721L;
	private List<StbStatusResponse> list;
	private int size;

	public STBStatusInfoResponse(List<StbStatusResponse> list, int size) {
		super();
		this.list = list;
		this.size = size;
	}

	public List<StbStatusResponse> getList() {
		return list;
	}

	public int getSize() {
		return size;
	}

	@Override
	public String toString() {
		return "STBStatusInfo [list=" + list + ", size=" + size + "]";
	}

}

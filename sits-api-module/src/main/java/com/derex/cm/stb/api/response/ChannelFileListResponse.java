package com.derex.cm.stb.api.response;

import java.io.Serializable;

public class ChannelFileListResponse implements Serializable {
	private static final long serialVersionUID = 8153802960202754470L;
	private String fileName;

	public String getFileName() {
		return fileName;
	}

	@Override
	public String toString() {
		return "ChannelFileListResponse [fileName=" + fileName + "]";
	}

	public ChannelFileListResponse(String fileName) {
		super();
		this.fileName = fileName;
	}

}
